"""Assessment quality metric - GEval-based evaluation of assessment reports."""

from pathlib import Path
from typing import Optional

import yaml
from deepeval.metrics import GEval
from deepeval.test_case import LLMTestCase, LLMTestCaseParams


class AssessmentQualityMetric(GEval):
    """Evaluates the quality of AC assessment reports (error detection mode).

    Uses LLM-as-judge to assess:
    - Surfaces all expected issues (no false negatives)
    - No false positives
    - Line/scenario references present and correct
    - Follows SKILL.md Assessment mode output format
    """

    def __init__(
        self,
        judge_model,
        expected_issues_path: Path,
        threshold: float = 0.8,
    ):
        """Initialize the metric.

        Args:
            judge_model: The configured LiteLLM judge instance
            expected_issues_path: Path to YAML file with expected issues list
            threshold: Minimum score to pass (0.8 = 80%)
        """
        # Load expected issues
        if not expected_issues_path.exists():
            raise FileNotFoundError(
                f"Expected issues file not found: {expected_issues_path}"
            )

        with open(expected_issues_path, "r", encoding="utf-8") as f:
            self.expected_data = yaml.safe_load(f)

        self.expected_issues = self.expected_data.get("expected_issues", [])

        # Build criteria
        criteria = f"""
You are evaluating the quality of an AC assessment report generated by the
accelint-ac-to-playwright skill in assessment mode.

The AC contains {len(self.expected_issues)} known issues. The assessment report
should detect all of them with no false positives.

Expected issues:
{self._format_expected_issues()}

Evaluate the following dimensions:

1. **Issue Detection Completeness**: Does the report surface all {len(self.expected_issues)}
   expected issues? No false negatives allowed.

2. **False Positive Rate**: Does the report flag any non-issues? False positives
   reduce trust in the assessment.

3. **Reference Accuracy**: Each reported issue must include a line number or
   scenario reference that correctly identifies where the problem occurs.

4. **Output Format Compliance**: The report must follow the SKILL.md template:
   - Starts with "❌ AC are not conversion-ready. Issues found:" (or "✓" if passing)
   - Each issue has: line/scenario reference, problem description, example quote, fix suggestion
   - Issues are numbered
   - Summary line at the end

5. **Actionability**: Issue descriptions are specific and actionable (not vague).
   Fix suggestions are clear and correct.
"""

        evaluation_steps = [
            "Parse the assessment report and extract all reported issues",
            "Compare reported issues against the expected issues list",
            "Check if all expected issues are detected (false negatives)",
            "Check if any non-issues are flagged (false positives)",
            "Verify line/scenario references are present and correct",
            "Verify output format follows SKILL.md template",
            "Assign a score from 0 to 1 based on completeness and accuracy",
        ]

        super().__init__(
            name="Assessment Quality",
            criteria=criteria,
            evaluation_steps=evaluation_steps,
            evaluation_params=[
                LLMTestCaseParams.ACTUAL_OUTPUT,
                LLMTestCaseParams.EXPECTED_OUTPUT,
            ],
            model=judge_model,
            threshold=threshold,
        )

    def _format_expected_issues(self) -> str:
        """Format expected issues for the judge's criteria."""
        lines = []
        for issue in self.expected_issues:
            line = issue.get("line", "?")
            desc = issue.get("issue", "")
            lines.append(f"  - Line {line}: {desc}")
        return "\n".join(lines)

    def measure(self, test_case: LLMTestCase, *args, **kwargs) -> float:
        """Measure assessment quality."""
        if not test_case.expected_output:
            test_case.expected_output = yaml.dump(self.expected_data)

        return super().measure(test_case, *args, **kwargs)

    async def a_measure(self, test_case: LLMTestCase, *args, **kwargs) -> float:
        """Async version."""
        if not test_case.expected_output:
            test_case.expected_output = yaml.dump(self.expected_data)

        return await super().a_measure(test_case, *args, **kwargs)
