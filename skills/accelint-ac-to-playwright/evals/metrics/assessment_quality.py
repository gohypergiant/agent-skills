"""Assessment quality metric - GEval-based evaluation of assessment reports."""

from pathlib import Path

import yaml
from deepeval.metrics import GEval
from deepeval.test_case import LLMTestCase, LLMTestCaseParams

# Rubric lives entirely in evaluation_steps (GEval: steps OR criteria, never
# both). The rubric is STATIC so it can be hashed; the per-fixture expected
# issues are dynamic and travel in expected_output instead (injected in
# measure()). No score scale in the steps — GEval's prompt owns the 0-10 scale.
RUBRIC_STEPS = [
    "You are evaluating the quality of an AC assessment report generated by "
    "the accelint-ac-to-playwright skill in assessment mode. The known "
    "expected issues for this AC are listed in expected_output; the report "
    "should detect all of them with no false positives.",
    "Parse the assessment report and extract all reported issues",
    "Compare reported issues against the expected issues in expected_output",
    "Check if all expected issues are detected (false negatives)",
    "Check if any non-issues are flagged (false positives)",
    "Verify line/scenario references are present and correctly identify where "
    "each problem occurs",
    "Verify output format follows the SKILL.md template: starts with '❌ AC "
    "are not conversion-ready. Issues found:' (or '✓' if passing); each issue "
    "has a line/scenario reference, problem description, example quote, and "
    "fix suggestion; issues are numbered; a summary line ends the report",
    "Verify issue descriptions are specific and actionable and fix "
    "suggestions are clear and correct",
]

# Recalibration tripwire (eval-architect audit check #12): sha256[:16] of
# "\n".join(RUBRIC_STEPS). Enforced by tests/test_rubric_hashes.py; a rubric
# edit requires updating this literal AND recalibrating the threshold.
RUBRIC_HASH = "d857541dd286424a"


class AssessmentQualityMetric(GEval):
    """Evaluates the quality of AC assessment reports (error detection mode).

    Uses LLM-as-judge to assess:
    - Surfaces all expected issues (no false negatives)
    - No false positives
    - Line/scenario references present and correct
    - Follows SKILL.md Assessment mode output format
    """

    # Class-level copies so record_metric call sites can pass
    # rubric_hash=metric.RUBRIC_HASH without extra imports.
    RUBRIC_HASH = RUBRIC_HASH
    RUBRIC_SOURCE = "metrics/assessment_quality.py"

    def __init__(
        self,
        judge_model,
        expected_issues_path: Path,
        threshold: float = 0.8,
    ):
        """Initialize the metric.

        Args:
            judge_model: The configured LiteLLM judge instance
            expected_issues_path: Path to YAML file with expected issues list
            threshold: Minimum score to pass (0.8 = 80%)
        """
        # Load expected issues
        if not expected_issues_path.exists():
            raise FileNotFoundError(
                f"Expected issues file not found: {expected_issues_path}"
            )

        with open(expected_issues_path, "r", encoding="utf-8") as f:
            self.expected_data = yaml.safe_load(f)

        self.expected_issues = self.expected_data.get("expected_issues", [])

        super().__init__(
            name="Assessment Quality",
            evaluation_steps=RUBRIC_STEPS,
            evaluation_params=[
                LLMTestCaseParams.ACTUAL_OUTPUT,
                LLMTestCaseParams.EXPECTED_OUTPUT,
            ],
            model=judge_model,
            threshold=threshold,
        )

    def measure(self, test_case: LLMTestCase, *args, **kwargs) -> float:
        """Measure assessment quality."""
        if not test_case.expected_output:
            test_case.expected_output = yaml.dump(self.expected_data)

        return super().measure(test_case, *args, **kwargs)

    async def a_measure(self, test_case: LLMTestCase, *args, **kwargs) -> float:
        """Async version."""
        if not test_case.expected_output:
            test_case.expected_output = yaml.dump(self.expected_data)

        return await super().a_measure(test_case, *args, **kwargs)
